export const CATCHMIND_WORDS: readonly string[] = [...NOUNS, ...ADJECTIVES, ...VERBS, ...ADVERBS];

export interface CatchmindRules {
  /** 승리에 필요한 점수 */
  targetScore: number;
  /** 한 라운드 제한시간(초) — 최대 60 */
  drawSeconds: number;
}

export const DEFAULT_CATCHMIND_RULES: CatchmindRules = { targetScore: 5, drawSeconds: 60 };

export const TARGET_SCORE_OPTIONS = [3, 5, 7] as const;
export const DRAW_SECONDS_OPTIONS = [30, 45, 60] as const;

export interface CatchmindState {
  /** 턴 순서 (player_id, 입장 순) */
  order: string[];
  /** player_id -> 맞힌 개수 */
  scores: Record<string, number>;
  /** 현재 그리는 사람 player_id */
  drawer: string;
  /** 현재 제시어 */
  word: string;
  /** 라운드 번호 — 바뀌면 모든 클라이언트가 캔버스를 지운다 */
  roundNo: number;
  /** 이번 라운드 마감 시각(ISO) */
  deadline: string;
}

export function pickWord(rand: () => number = Math.random): string {
  return CATCHMIND_WORDS[Math.floor(rand() * CATCHMIND_WORDS.length)];
}

function deadlineAt(drawSeconds: number): string {
  return new Date(Date.now() + drawSeconds * 1000).toISOString();
}

export function createCatchmindState(
  order: string[],
  rules: CatchmindRules,
  rand: () => number = Math.random,
): CatchmindState {
  const scores: Record<string, number> = {};
  for (const id of order) scores[id] = 0;
  return {
    order,
    scores,
    drawer: order[0],
    word: pickWord(rand),
    roundNo: 1,
    deadline: deadlineAt(rules.drawSeconds),
  };
}

/** 공백을 무시하고 비교한다 (예: "아이스 크림" == "아이스크림") */
export function normalizeGuess(s: string): string {
  return s.replace(/\s+/g, "").trim();
}

export function isCorrectGuess(word: string, guess: string): boolean {
  const n = normalizeGuess(guess);
  return n.length > 0 && n === normalizeGuess(word);
}

export interface GuessResult {
  state: CatchmindState;
  /** 이 정답으로 목표 점수에 도달했는지 */
  won: boolean;
}

/** 정답 처리: 맞힌 사람이 1점을 얻고 다음 출제자가 된다 */
export function applyCorrectGuess(
  state: CatchmindState,
  guesserId: string,
  rules: CatchmindRules,
  rand: () => number = Math.random,
): GuessResult {
  const newScore = (state.scores[guesserId] ?? 0) + 1;
  const scores = { ...state.scores, [guesserId]: newScore };
  return {
    state: {
      ...state,
      scores,
      drawer: guesserId,
      word: pickWord(rand),
      roundNo: state.roundNo + 1,
      deadline: deadlineAt(rules.drawSeconds),
    },
    won: newScore >= rules.targetScore,
  };
}

/** 시간 초과: 점수 없이 입장 순서상 다음 사람에게 차례가 넘어간다 */
export function applyTimeout(
  state: CatchmindState,
  rules: CatchmindRules,
  rand: () => number = Math.random,
): CatchmindState {
  const i = state.order.indexOf(state.drawer);
  const next = state.order[(i + 1) % state.order.length];
  return {
    ...state,
    drawer: next,
    word: pickWord(rand),
    roundNo: state.roundNo + 1,
    deadline: deadlineAt(rules.drawSeconds),
  };
}
